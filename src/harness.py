@dataclass
class Message:
    msg_id: str
    from_agent: str
    to_agent: str
    wire_form: str
    parsed: DslNode | None = None


@dataclass
class TaskState:
    task_id: str
    agent_id: str
    status: str = "created"  # created, dispatched, in_progress, complete, failed
    retries: int = 0
    result: str = ""  # DSL wire form


@dataclass
class RunResult:
    """Result of running a process."""
    nl_output: str
    messages: list[Message] = field(default_factory=list)
    task_states: dict[str, TaskState] = field(default_factory=dict)
    token_counts: dict[str, int] = field(default_factory=dict)

    @property
    def dsl_token_count(self) -> int:
        return sum(
            self.token_counts.get(m.msg_id, 0)
            for m in self.messages
            if m.msg_id != "M7"
        )

    @property
    def nl_token_count(self) -> int:
        return self.token_counts.get("M7", 0)

    @property
    def total_tokens(self) -> int:
        return self.dsl_token_count + self.nl_token_count


