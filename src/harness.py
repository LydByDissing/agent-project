def run_process(process: ProcessDefinition, user_input: str) -> RunResult:
    """Run a process end-to-end.

    Simulates the full multi-agent workflow:
    1. Main agent decomposes user input into tasks
    2. Dispatches tasks to subagents
    3. Collects results
    4. Aggregates and produces NL output

    Args:
        process: Loaded process definition
        user_input: Natural language input from user

    Returns:
        RunResult with NL output, messages, and token counts
    """
    result = RunResult(nl_output="")
    msg_counter = 0

    def next_msg_id() -> str:
        nonlocal msg_counter
        msg_counter += 1
        return f"M{msg_counter}"

    def record_msg(from_agent: str, to_agent: str, wire_form: str) -> Message:
        msg_id = next_msg_id()
        tokens = _count_tokens(wire_form)
        msg = Message(
            msg_id=msg_id,
            from_agent=from_agent,
            to_agent=to_agent,
            wire_form=wire_form,
            parsed=parse_dsl(wire_form),
        )
        result.messages.append(msg)
        result.token_counts[msg_id] = tokens
        return msg

    # ── Step 1: Main agent decomposes user input ──
    # (In production, this is an LLM call. For PoC, we use hardcoded tasks.)

    # ── Step 2: Dispatch to Coder (M1) ──
    coder_task = _build_coder_task()
    record_msg("main", "coder", coder_task)

    # ── Step 3: Coder produces result (M4) ──
    coder_result = SIMULATED_RESPONSES["t1"]
    record_msg("coder", "main", coder_result)

    # ── Step 4: Dispatch to Reviewer (M2) and Tester (M3) ──
    # Both reference t1's artifacts via context-ref
    reviewer_task = _build_reviewer_task()
    record_msg("main", "reviewer", reviewer_task)

    tester_task = _build_tester_task()
    record_msg("main", "tester", tester_task)

    # ── Step 5: Collect results (M5, M6) ──
    reviewer_result = SIMULATED_RESPONSES["t2"]
    record_msg("reviewer", "main", reviewer_result)

    tester_result = SIMULATED_RESPONSES["t3"]
    record_msg("tester", "main", tester_result)

    # ── Step 6: Aggregate results → NL (M7) ──
    coder_nl = dsl_to_nl(coder_result, "coder")
    reviewer_nl = dsl_to_nl(reviewer_result, "reviewer")
    tester_nl = dsl_to_nl(tester_result, "tester")

    aggregated = aggregate_results({
        "coder": coder_nl,
        "reviewer": reviewer_nl,
        "tester": tester_nl,
    })

    # Record M7 (NL output — token counted separately)
    m7_id = next_msg_id()
    result.nl_output = aggregated
    result.token_counts[m7_id] = _count_tokens(aggregated)
    result.messages.append(Message(
        msg_id=m7_id,
        from_agent="main",
        to_agent="user",
        wire_form=aggregated,
    ))

    return result


def run_process_from_file(process_path: str, user_input: str) -> RunResult:
    """Convenience: load process from file and run."""
    from src.process_loader import load_process
    proc = load_process(process_path)
    return run_process(proc, user_input)
